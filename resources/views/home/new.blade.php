                <h2 class="c_titile">愛情不容有錯，即使錯了那就重來</h2>
                
            <p class="box_c">
                <span class="d_time">發佈時間：2016-09-08 </span>
                <span>編輯：後盾</span> <span>查看次數：2323 </span>
            </p>
                
            <ul class="infos">
                      
                <p>
                    進一好友空間，再一次看到她置頂的那一篇"他和她的故事"愛情在他倆的生活中經歷了不少分分合合，但最後倆人還是走在了一起當今天再一次閱讀的時候，我沒有第一次那麼感性，我注重他們倆曲折的過程，其中出現過第三者... </p>
                      <p>
                    如果我是她，對於他當初的腳踏兩隻船，儘管仍然說愛但亦然離開而選擇別人，我很肯定我是無法原諒他的，也許你會說是因為我不是真正愛他吧，我也不知道，這是不是因為愛得不夠深，不能包容他的過錯...我是很小氣的人，就像很多人說的那樣，一年365天，你364天對她好，只要有一天對她不好，她也會在n天也許n年後提出來，你哪天對我不好。</p>
                      <p><img src="images / e5e39b1148811261c78b933805d07232.png" alt="分手"></p>
                      跟他交交，會有意無意的傷到我自尊，至今我也那麼認為那傷害了我的自尊。我的小朋友，我至今也覺得如果我不小氣，那就是我傻！</p>
                      <p>我認為大學時的戀愛，是畢業前進入社會的一次戀愛實習。參雜工作，從銷售到文員到編輯到技術員，最後才知道什麼工作適合自己。找一個人陪你戀愛實習，也許很簡單，也許很難。</p>
                      現在我們各自有了各自的生活，他也找到了他心儀的女孩，我能感覺出他們倆很相愛，當初最後一次分手的過渡時間，無論有多麼不捨與難熬，現在看看都是正確的。</p>
                      <p><img src="images / 1f8aea2172f6eb987c3de3f7ba474eb1.png" alt="分手"></p>
                      <p>為何我會毅然放棄，因為在我心裡，那些過去的傷，那些不好的記憶永遠都抹不去，我無法面對，哪怕是當初妥協了，我想最後我也一定會再一次選擇放棄.... </p>
                      <p>我覺得愛情也可以重新過來，只是換換主角罷了... </p>
                    
